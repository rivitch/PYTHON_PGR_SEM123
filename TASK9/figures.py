class Rectangle:
    def __init__(self, width, height=0) -> None:
        self.width = width
        self.height = height if height else width
        
    def calculate_area(self) -> float:
        return self.width * self.height
    
    def calculate_perimeter(self) -> float:
        return (self.width + self.height) * 2


